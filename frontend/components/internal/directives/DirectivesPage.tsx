"use client";

import { useState } from "react";
import data from "@/lib/data/internal/strategic-accountability.json";
import { LensPageHeader } from "@/components/internal/shared/LensPageHeader";
import { SectionHeader } from "@/components/internal/shared/SectionHeader";
import { AssessmentBlock } from "@/components/internal/shared/AssessmentBlock";
import { StatusSummaryBar } from "@/components/internal/strategic-accountability/StatusSummaryBar";
import { DirectiveCardGroup } from "@/components/internal/strategic-accountability/DirectiveCardGroup";
import { DirectiveModal } from "@/components/internal/strategic-accountability/DirectiveModal";
import type { Directive, DirectiveStatus } from "@/lib/types/internal-intelligence";

const directives = data.directives as Directive[];

const STATUS_GROUP_ORDER: DirectiveStatus[] = [
  "Overdue",
  "Blocked",
  "In progress",
  "On track",
  "Not started",
  "Completed",
];

export function DirectivesPage() {
  const [selected, setSelected] = useState<Directive | null>(null);
  const [modalOpen, setModalOpen] = useState(false);

  function handleCardClick(directive: Directive) {
    setSelected(directive);
    setModalOpen(true);
  }

  const grouped = STATUS_GROUP_ORDER.map((status) => ({
    status,
    directives: directives.filter((d) => d.status === status),
  }));

  return (
    <div className="space-y-10">
      <LensPageHeader
        lensName="Directives"
        narrativeOwner={data.narrativeOwner}
        lastUpdated={data.lastUpdated}
        isAutoGeneratedOnly={data.isAutoGeneratedOnly}
      />

      <div>
        <SectionHeader title="Assessment" />
        <AssessmentBlock assessment={data.assessment} />
      </div>

      <div>
        <SectionHeader title="Execution Status" />
        <StatusSummaryBar directives={directives} />
      </div>

      <div className="space-y-8">
        {grouped.map(({ status, directives: group }) => (
          <DirectiveCardGroup
            key={status}
            status={status}
            directives={group}
            onCardClick={handleCardClick}
          />
        ))}
      </div>

      <DirectiveModal
        directive={selected}
        open={modalOpen}
        onOpenChange={setModalOpen}
      />
    </div>
  );
}
